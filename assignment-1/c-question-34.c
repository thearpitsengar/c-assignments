// Q34. What are comments and how do you insert it in a C program?

// Ans- 
// Comments in C are used to provide information about the code.
// They can be used to explain what the code does, to document the code,
// or to disable code that is not needed. Comments are ignored by the compiler,
// so they do not affect the execution of the program. However,
// they can be very useful for making the code more readable and understandable.

// There are two types of comments in C:

// 1. Single-line comments: These comments start with two forward slashes (//)
// and extend to the end of the line.

// 2. Multi-line comments: These comments start with a forward slash and
// an asterisk (/) and end with an asterisk and a forward slash (/).




